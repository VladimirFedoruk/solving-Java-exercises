public class IfStairs {
    public static void main(String[] args) {
        for (int x = 0; x < 4; x++){
            if (x == 1)
                System.out.println("один");
            else if (x == 2)
                System.out.println("два");
            else if (x == 3)
                System.out.println("три");
            else
                System.out.println("ноль");

        }

    }
}
